package day9_28;
/**
 * 定义长度为5的字符串数组 分别赋值为：apple orange banana pear  tomato .使用循环显示数组的每个元素
 * 
 * @author WangXin
 *
 */

public class s3 {
	public static void main(String[] args) {
		String[] a = {"apple","orange","banana","pear","tomato"};
		
		for(int i = 0; i< a.length; i++){
			System.out.print(a[i] + "\t");
			
		}
	}

}
